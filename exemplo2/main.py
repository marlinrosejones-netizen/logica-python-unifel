# Prática de TIPAGEM DINAMICA
x = 10 # x aponta para um objeto int (heap)
print(type(x)) # Mostra a classe do objeto no momento!!
x = "Minha turma top UNIFEL!!"
print(type(x)) # Agora x aponta para um objeto str (heap)
# Python é dinamicamente tipada, ou seja, o tipo de uma variável é determinado
# em tempo de execução e pode mudar durante a execução do programa. Nunca se esqueça
